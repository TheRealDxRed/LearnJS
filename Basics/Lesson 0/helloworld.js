// We can print messages to the console like this:
console.log("Hello World!");
/*
	In some programming languages like C and C++, you have to tell the computer to start a new
	line after printing the string. In JavaScript, "console.log" handles this for us, making our
	code more readable as well as making JavaScript easier to pick up and learn.
*/

// We can also store the text we want to print as a variable like this...
var greeting = "Hello World!";
// ...and then print it out using the same "console.log"
console.log(greeting);
